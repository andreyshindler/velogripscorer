// Default to Hebrew: the public results/standings links are shared with a
// Hebrew-speaking audience. Anyone can switch to English with the toggle.
let LANG = localStorage.getItem('lang') || 'he';

function t(key, vars) {
  let s = (I18N[LANG] && I18N[LANG][key]) || I18N.en[key] || key;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(`{${k}}`, v);
  return s;
}

function setLang(lang) {
  LANG = I18N[lang] ? lang : 'en';
  localStorage.setItem('lang', LANG);
  document.documentElement.lang = LANG;
  document.documentElement.dir = I18N[LANG].dir;
  document.querySelectorAll('[data-i18n]').forEach((el) => (el.textContent = t(el.dataset.i18n)));
  const toggle = document.getElementById('lang-toggle');
  if (toggle) toggle.textContent = LANG === 'en' ? 'עברית' : 'English';
}

function fmtDate(iso) {
  if (!iso) return '—';
  try {
    return new Date(iso).toLocaleString(LANG === 'he' ? 'he-IL' : 'en-US', {
      dateStyle: 'medium', timeStyle: 'short', hour12: false, // 24-hour clock
    });
  } catch { return iso; }
}
